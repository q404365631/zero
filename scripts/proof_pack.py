def write_demo_readme(path: Path) -> None:
    path.write_text(
        """# ZERO Demo Proof Pack

This directory is generated by `scripts/proof_pack.py`.

It is a deterministic paper-mode proof pack for repository launch readiness.
It is not a paper-vs-live P&L correlation claim and does not imply live trading
profitability.

Verify it with:

```bash
PYTHONPATH="$PWD/engine/src" scripts/proof_pack.py --check
```
""",
        encoding="utf-8",
    )


def build(output_dir: Path) -> None:
    output_dir.mkdir(parents=True, exist_ok=True)
    paper = run_paper_scenario()

    decisions_csv = output_dir / "paper-decisions.csv"
    proof_svg = output_dir / "paper-proof.svg"
    demo_readme = output_dir / "README.md"
    manifest = output_dir / "proof-pack.json"

    write_decisions_csv(decisions_csv, paper)
    write_svg(proof_svg, paper)
    write_demo_readme(demo_readme)

    artifacts = {
        "paper_decisions_csv": sha256_file(decisions_csv),
        "paper_proof_svg": sha256_file(proof_svg),
        "demo_readme": sha256_file(demo_readme),
        "scenario_json": sha256_file(SCENARIO_PATH),
        "candles_jsonl": sha256_file(CANDLES_PATH),
    }
    payload = {
        "schema_version": "zero.proof_pack.v1",
        "generated_at": GENERATED_AT,
        "name": "demo-paper-launch-smoke",
        "mode": "paper",
        "claim_boundary": {
            "paper_mode_verified": True,
            "live_trading_claimed": False,
            "paper_vs_live_correlation_claimed": False,
            "pnl_claimed": False,
        },
        "paper": {
            "scenario": paper["scenario"],
            "decisions": len(paper["decisions"]),
            "fills": paper["fills"],
            "rejections": paper["rejections"],
            "open_positions": len(paper["positions"]),
            "symbols": sorted(paper["market"].keys()),
        },
        "live_correlation": {
            "status": "unavailable",
            "reason": "requires signed paper/live records and exchange-side evidence",
            "r_squared": None,
        },
        "privacy": {
            "contains_exchange_credentials": False,
            "contains_wallet_material": False,
            "contains_raw_exchange_order_ids": False,
            "contains_private_notes": False,
        },
        "artifacts": artifacts,
    }
    encoded = json.dumps(payload, indent=2, sort_keys=True).encode("utf-8")
    payload["proof_hash"] = f"sha256:{hashlib.sha256(encoded).hexdigest()}"
    manifest.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n", encoding="utf-8")


def compare_dirs(current: Path, expected: Path) -> int:
    status = 0
    paths = sorted({path.relative_to(current) for path in current.rglob("*") if path.is_file()})
    paths.extend(
        path.relative_to(expected)
        for path in expected.rglob("*")
        if path.is_file() and path.relative_to(expected) not in paths
    )
    for rel in sorted(set(paths)):
        left = current / rel
        right = expected / rel
        left_text = left.read_text(encoding="utf-8") if left.exists() else ""
        right_text = right.read_text(encoding="utf-8") if right.exists() else ""
        if left_text == right_text:
            continue
        status = 1
        diff = difflib.unified_diff(
            left_text.splitlines(),
            right_text.splitlines(),
            fromfile=str(left),
            tofile=str(right),
            lineterm="",
        )
        print("\n".join(list(diff)[:200]), file=sys.stderr)
    return status


def main() -> int:
    parser = argparse.ArgumentParser()
    parser.add_argument("--check", action="store_true", help="fail if generated proof pack is stale")
    args = parser.parse_args()

    if args.check:
        with tempfile.TemporaryDirectory() as tmp:
            expected = Path(tmp) / "demo"
            build(expected)
            status = compare_dirs(OUTPUT_DIR, expected)
            if status:
                print("docs/proof/demo is stale; run scripts/proof_pack.py", file=sys.stderr)
            return status

    if OUTPUT_DIR.exists():
        shutil.rmtree(OUTPUT_DIR)
    build(OUTPUT_DIR)
    print(f"wrote {OUTPUT_DIR.relative_to(ROOT)}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
