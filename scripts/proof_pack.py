#!/usr/bin/env python3
"""Build the public-safe demo proof pack."""

from __future__ import annotations

import argparse
import csv
import difflib
import hashlib
import json
from pathlib import Path
import shutil
import sys
import tempfile
from xml.sax.saxutils import escape

from zero_engine import JsonlCandleAdapter, PaperEngine, load_scenario

ROOT = Path(__file__).resolve().parents[1]
OUTPUT_DIR = ROOT / "docs" / "proof" / "demo"
SCENARIO_PATH = ROOT / "examples" / "paper-trading" / "scenario.json"
CANDLES_PATH = ROOT / "examples" / "paper-trading" / "candles.jsonl"
PAPER_TS = 1777646400.0
GENERATED_AT = "2026-05-01T00:00:00Z"


def sha256_file(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return f"sha256:{digest.hexdigest()}"


def run_paper_scenario() -> dict:
    scenario = load_scenario(SCENARIO_PATH)
    market = JsonlCandleAdapter(CANDLES_PATH)
    engine = PaperEngine(limits=scenario.limits, clock=lambda: PAPER_TS)

    for order in scenario.orders:
        engine.submit(order, source=f"scenario:{scenario.name}")

    symbols = sorted({order.symbol for order in scenario.orders})
    return {
        "scenario": scenario.name,
        "mode": scenario.mode,
        "market": {
            symbol: {
                "as_of": market.latest(symbol).ts,
                "last": market.latest(symbol).close,
            }
            for symbol in symbols
        },
        "fills": len(engine.fills),
        "rejections": len(engine.rejections),
        "positions": {
            symbol: {
                "quantity": position.quantity,
                "avg_price": position.avg_price,
                "notional_usd": position.notional_usd,
            }
            for symbol, position in sorted(engine.positions.items())
        },
        "decisions": [record.to_dict() for record in engine.decisions],
    }


def write_decisions_csv(path: Path, paper: dict) -> None:
    fieldnames = (
        "as_of",
        "source",
        "symbol",
        "side",
        "quantity",
        "price",
        "notional_usd",
        "confidence",
        "reduce_only",
        "allowed",
        "reason",
    )
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fieldnames)
        writer.writeheader()
        for decision in paper["decisions"]:
            writer.writerow({key: decision[key] for key in fieldnames})


